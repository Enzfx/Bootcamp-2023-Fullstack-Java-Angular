package edu.enzo.tiposevariaveis;
        // Constantes
        // Em Java, constantes são declaradas usando a palavra-chave 'final'
        // Por convenção, os nomes de constantes são escritos em letras maiúsculas
public class Constantes {
    public static void main(String[] args) {
        final int NUMERO = 10; // Constante inteira
        final double PI = 3.14; // Constante de ponto flutuante
        final String NOME = "Enzo"; // Constante de string

        // Exibindo os valores das constantes
        System.out.println("Número: " + NUMERO);
        System.out.println("Pi: " + PI);
        System.out.println("Nome: " + NOME);
    }
}

